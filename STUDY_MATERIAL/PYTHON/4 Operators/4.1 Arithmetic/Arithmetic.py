'''
Arithmetic Operators:

    Addition +
    Subtraction -
    Multiplication *
    Division /
    Modulus %
    Exponentiation **
    Floor Division //

'''
a = 10
b = 5
print(a + b)  # Output: 15
print(a - b)  # Output: 5
print(a * b)  # Output: 50
print(a / b)  # Output: 2.0
print(a // b) # Output: 2
print(a ** b) # Output: 100000 (10*10*10*10*10)
print(a % b)  # Output: 0 (remainder)