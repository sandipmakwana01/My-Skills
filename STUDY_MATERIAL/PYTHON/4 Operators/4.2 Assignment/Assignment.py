'''
when using compound assignment operators (+=, -= , *= , /=). In Python, these compound assignment operators are not used with the print statement directly.

not use:-
x=5
print(x+=3)

use this:-
x=5
x+=3
print(x)
'''

'''
Assignment Operators:

    Assignment =
    Add and assign +=
    Subtract and assign -=
    Multiply and assign *=
    Divide and assign /=
    Modulus and assign %=
    Exponentiate and assign **=
    Floor divide and assign //=

'''
x = 5  
print(x) # Output: 5

x += 3   # x = x + 3
print(x) # Output: 8

x -= 2   # x = x - 2
print(x) # Output: 3

x *= 2   # x = x * 2
print(x) # Output: 10

x /= 2   # x = x / 2
print(x) # Output: 2.5

x //=2   # x = x // 2
print(x) # Output: 2

x **= 2  # x = x ** 2  # (5*5)
print(x) # Output: 25


