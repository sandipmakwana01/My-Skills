'''
    Numeric Types:
        int: Integer type, represents whole numbers (e.g., 5, -10, 1000).
        float: Floating-point type, represents decimal numbers (e.g., 3.14, -0.001, 2.0).

    Sequence Types:
        str: String type, represents sequences of characters (e.g., "hello", 'world').
        list: List type, ordered collection of items (e.g., [1, 2, 3], ['apple', 'banana', 'orange']).
        tuple: Tuple type, ordered collection of items (similar to lists but immutable) (e.g., (1, 2, 3), ('a', 'b', 'c')).

    Mapping Type:
        dict: Dictionary type, unordered collection of key-value pairs (e.g., {'name': 'John', 'age': 30}).

    Set Types:
        set: Set type, unordered collection of unique items (e.g., {1, 2, 3}, {'apple', 'banana', 'orange'}).
        frozenset: Immutable version of set (e.g., frozenset({1, 2, 3})).

    Boolean Type:
        bool: Boolean type, represents truth values (True or False).

    None Type:
        None: Represents the absence of a value or a null value.

    Sequence Types for Binary Data:
        bytes: Immutable sequence of bytes (e.g., b'hello').
        bytearray: Mutable sequence of bytes (e.g., bytearray(b'hello')).

    Other Types:
        complex: Complex number type, represents complex numbers (e.g., 3 + 4j).
'''